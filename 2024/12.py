rows = []
for inputRow in rawInput.split('\n'):
    rows.append(list(inputRow))

height = len(rows)
width = len(rows[0])

class Region:
    def __init__(self, id):
        self.id = id
        self.positions = []

        self.area = 0
        self.perimiter = 0
        self.edgePositions = []
        self.sides = 0


    def clear(self):
        for pos in self.positions:
            rows[pos[0]][pos[1]] = '.'

    def expand(self, pos):
        self.area += 1
        self.positions.append(pos)

        for dir in [[1 ,0], [-1, 0], [0, 1], [0, -1]]:
            newPos = [pos[0] + dir[0], pos[1] + dir[1]]

            if newPos[0] < 0 or newPos[1] < 0 or newPos[0] >= height or newPos[1] >= width:
                self.addEdge(pos[0], pos[1], dir)
                continue

            if rows[newPos[0]][newPos[1]] == self.id:
                
                isExisting = False
                for existingPos in self.positions:
                    if newPos[0] == existingPos[0] and newPos[1] == existingPos[1]:
                        isExisting = True
                        break
                if not isExisting:
                    self.expand(newPos)
            else:
                self.addEdge(pos[0], pos[1], dir)

    def addEdge(self, i, j, dir):
        self.perimiter += 1

        h = dir[1] == 0
        newEdge = [i + dir[0]/2, j + dir[1]/2, dir[0] if h else dir[1]]
        adjacentEdges = []
        for e in self.edgePositions:
            if h and e[0] == newEdge[0] and e[2] == newEdge[2]:
                if e[1] == newEdge[1] + 1 or e[1] == newEdge[1] - 1:
                    adjacentEdges.append(e)
            if not h and e[1] == newEdge[1] and e[2] == newEdge[2]:
                if e[0] == newEdge[0] + 1 or e[0] == newEdge[0] - 1:
                    adjacentEdges.append(e)

        if len(adjacentEdges) == 0:
            self.sides +=1
        elif len(adjacentEdges) == 2:
            self.sides -= 1

        self.edgePositions.append(newEdge)


